		<h1 class="decay">Bio</h1>
		<p>O Symphony of Thunder existe a aproximadamente 5 Anos.
A idéia de banda surgiu da parte de Julio (Batera / Vocal) , e Tom (Guitar / Vocal), que tinham intenção de montar um projeto na linha Heavy Metal, que era o que eles ouviam na época. Após algum tempo, e alguns covers, a formação se estabiliza com a entrada Elton (Irmão de Tom), assumindo o Baixo. Algumas composições proprias surgiram e com isso , sentiram a necessidade de ter uma segunda guitarra, para acrescentar mais ao som da banda. Nessa mesma época, conhecem Renan (Baixista) e o convidam para se juntar a banda. Nisso, Elton assume a Segunda Guitarra e Renan, o Baixo.
As composições da banda começam a tomar forma, seguindo uma direção diferente, com Julio fazendo vocais Screams junto ao peso e melodia das guitarras. O som da banda se torna uma mistura de varios elementos dentro do metal. 
Após varias apresentações em sua região, a banda percebe que está na hora de gravar um material com as músicas que tinham. E com isso, mais mudanças na Line-Up , Roni, assume os teclados da banda, dando mais diversidade e melodia as composições.
Pouco tempo antes da tão esperada gravação, a banda sofre uma baixa. O Guitarrista Elton decide sair da banda.
As gravações começam. Julio e Renan gravam as partes de guitarra que eram feitas por Elton. Após o término das gravações , a banda começa a confeccionar o material, Um Ep intitulado "Perseverance" , com 5 faixas, entre elas, "In Flames", que recentemente participou da coletânea extrema - Extreme Collection V - da revista Extreme Brutal Death.
Nesse período, a banda apresenta seu novo guitarrista, Adriano , mais conhecido como "Miojão".
Poucas semanas antes do lançamento do Ep, Roni muda-se de cidade e deixa a banda.
A banda lança seu Ep, e recebe boas críticas da mídia especializada, que classificam o som como um Black Metal Melódico com uso de teclados, aliado a elementos Heavy, Thrash e Power.
Com isso, a banda começa a preparar novas composições, dando prioridade á agressividade e ao peso, e ao mesmo tempo, melodia nas guitarras, sem abrir mão dos elementos que usavam antes, como dedilhados e vocais limpos, alternados com os Vocais Screams.
Após vários shows em várias locais do estado de São Paulo, como Vale do Ribeira, Litoral Sul, Interior e também na Capital do estado, a banda resolve registrar uma musica nova, "Black Feelings of Mind", mostrando uma progressão acentuada em relação as músicas do Ep.
Entrando em 2007. a banda se prepara para gravar um vídeoclip, e também começa a produção de seu primeiro albúm cheio.


	

